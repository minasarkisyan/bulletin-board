<?php

namespace App\Models\Adverts;

use Illuminate\Database\Eloquent\Model;
use Kalnoy\Nestedset\NodeTrait;

/**
 * @property int $id	 * @property int $id
 * @property string $name	 * @property string $name
 * @property string $slug	 * @property string $slug
 * @property int|null $parent_id	 * @property int|null $parent_id
 **
 * @property int $depth	 * @property int $depth
 * @property Category $parent	 * @property Category $parent
 * @property Category[] $children	 * @property Category[] $children
 * @property Attribute[] $attributes
 */
class Category extends Model
{
    use NodeTrait;

    protected $table = 'advert_categories';

    public $timestamps = false;

    protected $fillable = ['name', 'slug', 'parent_id'];

    public function attributes()
    {
        return $this->hasMany(Attribute::class, 'category_id', 'id');
    }
}
